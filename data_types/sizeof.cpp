#include<iostream>

using namespace std;

int main()
{

     cout << "The size of the integer is " << sizeof(int) << endl;
     cout << "The size of the float is " << sizeof(float) << endl;
     cout << "The size of the double is " << sizeof(double) << endl;
     cout << "The size of the char is " << sizeof(char) << endl;
     cout << "The size of the string is " << sizeof(string) << endl;
     cout << "The size of the pointer is " << sizeof(int*) << endl;

return 0;

}
